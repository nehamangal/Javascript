//Number to string
let myvar;
myvar  = String(10);
console.log(myvar)                   // Return 10

// Boolean to String
let boolVar = true;
boolVar = String(boolVar)
console.log(boolVar)                 // Return true

// Date in String
let date = String(new Date())
console.log(date)                 // Return Sun Aug 07 2022 23:04:24 GMT+0530 (India Standard Time)


// Array to String
let arr = String([1,2,3,4])
console.log(arr.length);         // Return 7