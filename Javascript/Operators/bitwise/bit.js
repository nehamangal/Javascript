
//  &
var a = 5;
var b = 1;
console.log(a & b);   //Return 1 

//  ||

var a = 5;
var b = 1;
console.log(a || b);     // Return 5

//  ^

var a = 5;
var b = 1;
console.log(a ^ b);     // Return 4

//  <<

var a = 5;
var b = 1;
console.log(a <<b);     // Return 10

//  >>

var a = 5;
var b = 1;
console.log(a >>b);     // Return 2

