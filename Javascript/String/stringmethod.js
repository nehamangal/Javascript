// String length
var s = "nehamnagal"
console.log(s.length)             // Output is :10

//charCodeAt
var s = "neha mangal "
console.log(s.charCodeAt(3))         // Return unicode value of 'a' that is 97


// tolower
var s = "neha mangal"
console.log(s.toLocaleUpperCase())     // Return NEHA MANGAL

// Substr
var s = "my name is neha mangal"
console.log(s.substring(3,10))         // Return name is

// Slice
var s = "apple,mangal,banana"
console.log(s.slice(6,13))            // Return mangal,


// Indexof
var s = "neha mangal"
console.log(s.indexOf('a'))               // Return 3

// Lastindexof
var s = "neha mangal neha mangal"
console.log(s.lastIndexOf("mangal"))         // Return 17


  